Membangun Pondasi Pendidikan yang Kuat
Sejak awal berdirinya, SDN 012 Babakan Ciparay telah berfokus pada pengembangan pondasi pendidikan yang kuat bagi siswa. Dengan motto "Menumbuhkan Cinta Belajar, Menyiapkan Generasi Berkarakter," sekolah ini berusaha menciptakan lingkungan belajar yang kondusif dan mendukung setiap siswa untuk berkembang sesuai dengan potensi mereka. Kepala Sekolah SDN 012 Babakan Ciparay, Ibu Siti Nurhayati, menjelaskan, "Pendidikan yang baik dimulai dari lingkungan yang baik. Kami selalu berusaha memberikan fasilitas yang memadai serta pengajaran yang menarik dan relevan dengan perkembangan zaman." <br> <br> <br>

Untuk mencapainya, SDN 012 Babakan Ciparay telah mengimplementasikan berbagai inovasi dalam pengajaran yang tidak hanya terbatas pada aspek akademik, tetapi juga pada pengembangan karakter siswa. Salah satu program unggulan yang sedang digalakkan adalah Pengembangan Karakter Positif. Program ini bertujuan untuk membentuk siswa yang memiliki sikap jujur, disiplin, bertanggung jawab, dan peduli terhadap sesama. Program ini dilaksanakan melalui kegiatan-kegiatan rutin di sekolah, seperti Pendidikan Karakter yang melibatkan orang tua, serta kegiatan sosial yang melatih empati siswa terhadap kondisi sosial sekitar. <br><br><br>

Kemitraan dengan Universitas Pendidikan Indonesia: Meningkatkan Kualitas Pengajaran
Sejalan dengan perkembangan pendidikan yang semakin berbasis pada teknologi, SDN 012 Babakan Ciparay juga aktif melakukan kemitraan dengan berbagai pihak, termasuk dengan Universitas Pendidikan Indonesia (UPI). Kemitraan ini dimulai pada tahun 2023, dengan tujuan untuk meningkatkan kualitas pendidikan di tingkat sekolah dasar, khususnya dalam hal pengajaran berbasis teknologi dan metode pembelajaran yang lebih efektif. Pada bulan Desember 2023, UPI mengadakan pelatihan untuk guru-guru SDN 012 Babakan Ciparay yang berfokus pada pemanfaatan teknologi dalam pembelajaran. <br><br><br>

"Melalui kemitraan dengan UPI, kami berharap para guru kami dapat memanfaatkan teknologi untuk meningkatkan efektivitas dan interaktivitas dalam mengajar. Salah satunya adalah penggunaan platform pembelajaran digital yang memudahkan siswa untuk mengakses materi pelajaran secara lebih fleksibel dan menyenangkan," ujar Ibu Siti Nurhayati. Salah satu hasil dari kemitraan ini adalah penggunaan aplikasi e-learning yang memungkinkan siswa untuk mengakses tugas, latihan, dan materi ajar secara daring, yang sangat bermanfaat di era pembelajaran digital seperti sekarang ini. <br><br><br>

Selain itu, UPI juga memberikan program bimbingan dan workshop tentang metode pengajaran yang berbasis pada kebutuhan siswa, serta cara-cara untuk mengembangkan kreativitas siswa melalui penugasan proyek yang bersifat kolaboratif. Kerjasama ini diharapkan dapat membantu meningkatkan kualitas pengajaran serta memberi pengalaman baru bagi para guru untuk terus berkembang dalam profesinya. <br><br><br>
